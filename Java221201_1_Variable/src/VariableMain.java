
public class VariableMain {

	public static void main(String[] args) {
		// 사람 나이 25살 이라는 데이터를 임시 저장
		int age;
		// 사람 나이에 25 저장하기
		age = 25;
		System.out.println("age");
		System.out.println(age);
		System.out.println("------");

		// 사람 시력을 저장해야 할 상황
		double eye; // 변수 선언
		// 사람 시력에 1.5 저장
		eye = 1.5; // 변수 값 대입
		// double eye = 1.5; // 선업 + 대입
		// float eye = 1.5f; 
		System.out.println(eye);
		System.out.println("------");
		
		// 똥이라는 문자 저장
		char ddong = '똥';
		System.out.println(ddong);
		System.out.println("------");
		
		// 사람 이름 이서연 저장
		String name = "이서연";
		System.out.println(name);
		System.out.println("------");
		
		//논리
		boolean cold = true; //false
		System.out.println("cold");
		System.out.println("------");
	}

}
